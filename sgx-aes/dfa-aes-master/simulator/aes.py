def to_matrix(s):
  m = []
  for i in xrange(4):
    m.append([(s >> (120-8*(4*j+i))) & 0xff for j in xrange(4)])
  return m

def to_bitstring(s):
  m = 0
  for i in xrange(len(s)):
    for j in xrange(len(s[i])):
      m ^= s[i][j] << (120-8*(4*j+i))
  return m

